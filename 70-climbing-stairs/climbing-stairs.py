class Solution:
    def climbStairs(self, n: int) -> int:
        """
        1 : 1 : 1
        2 : 11, 2 : 2
        3 : 111, 12, 21 : 3
        4 : 1111, 112, 121, 211, 22 : 5 
        5 : 11111, 1112, 1121, 1211, 2111, 221, 212, 122 : 8 
        6 : 111111, 11112, 11121, 11211, 12111, 21111, 1122, 1212, 1221, 2112, 2121, 2211, 222 : 13
        # 1은 고정적으로 1
        # 2는 n // 2 의 몫에 따라 1 2 3 4 로 나뉘고
        # n % 2 가 0 이면 1 + 2 * n//2, 1 이면 1 + 2 * (n//2 + 1)
        """
        if n <= 3 :
            return n
            
        dp = [0] * (n + 1)
        dp[1] = 1
        dp[2] = 2

        for i in range(3, n + 1) :
            dp[i] = dp[i-2] + dp[i-1]
        return dp[-1]